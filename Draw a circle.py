#Draw a circle using Turtle
import turtle

#Change penzie to 5
turtle.pensize (5)
#change pen color for the circle to RED
turtle.pencolor ("red")
#Change the start point
turtle.Turtle().goto(-40, 120)
#Change color to Blue
turtle.fillcolor ("blue")
turtle.begin_fill ()

#Draw a circle with 100 raidus
turtle.circle (100)
turtle.end_fill ()
turtle.done
